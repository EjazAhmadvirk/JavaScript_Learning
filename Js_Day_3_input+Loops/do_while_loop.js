let i = 0;
do {
    console.log(`Current value of i: ${i}`);
    i++;
} while (i < 5);
// This code initializes a variable i to 0 and uses a do-while loop to print the current value of i
// until i is no longer less than 5. The loop increments i by 1 in each iteration, demonstrating a simple use of a do-while loop in JavaScript.